#include <memory>

#include <libtcod.hpp>

#include "Item.hpp"
#include "Level.hpp"
#include "Player.hpp"
#include "PlayerController.hpp"
#include "Tile.hpp"

namespace
{
	const int WINDOW_WIDTH_CELLS = 80;
	const int WINDOW_HEIGHT_CELLS = 45;
	const int MAP_WIDTH_CELLS = 60;
	const int MAP_HEIGHT_CELLS = 45;
}

void Initialize(Level& level, Tileset& tileSet, Itemset& itemSet, Player& player, PlayerController& playerController);
void Draw(const Tileset& tileSet, const TileMap& terrain, const Player& player);

int main()
{
	// Initialize game window.
	TCODConsole::setCustomFont("terminal16x16_gs_ro.png", TCOD_FONT_LAYOUT_ASCII_INROW, 16, 16);
	TCODConsole::initRoot(WINDOW_WIDTH_CELLS, WINDOW_HEIGHT_CELLS, "Roguelike");

	Level level;
	Itemset itemSet;
	Tileset tileSet;
	Player player;
	PlayerController playerController;
	
	// Initial data setup.
	Initialize(level, tileSet, itemSet, player, playerController);

	TCOD_key_t key;
	bool running = true;

	while (running && !TCODConsole::isWindowClosed())
	{
		Draw(tileSet, level.terrain, player);

		auto inputEvent = TCODSystem::checkForEvent(TCOD_EVENT_KEY_PRESS, &key, NULL);
		if (inputEvent == TCOD_EVENT_KEY_PRESS)
		{
			switch (key.vk)
			{
			case TCOD_keycode_t::TCODK_ESCAPE:
				running = false;
				break;
			case TCOD_keycode_t::TCODK_KP8:
				playerController.HandleAction(InputCommand::MoveNorth, tileSet, level.terrain);
				break;
			case TCOD_keycode_t::TCODK_KP9:
				playerController.HandleAction(InputCommand::MoveNE, tileSet, level.terrain);
				break;
			case TCOD_keycode_t::TCODK_KP6:
				playerController.HandleAction(InputCommand::MoveEast, tileSet, level.terrain);
				break;
			case TCOD_keycode_t::TCODK_KP3:
				playerController.HandleAction(InputCommand::MoveSE, tileSet, level.terrain);
				break;
			case TCOD_keycode_t::TCODK_KP2:
				playerController.HandleAction(InputCommand::MoveSouth, tileSet, level.terrain);
				break;
			case TCOD_keycode_t::TCODK_KP1:
				playerController.HandleAction(InputCommand::MoveSW, tileSet, level.terrain);
				break;
			case TCOD_keycode_t::TCODK_KP4:
				playerController.HandleAction(InputCommand::MoveWest, tileSet, level.terrain);
				break;
			case TCOD_keycode_t::TCODK_KP7:
				playerController.HandleAction(InputCommand::MoveNW, tileSet, level.terrain);
				break;
			}
		}	
	}
}

