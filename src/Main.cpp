void Draw(const Tileset& tileSet, const TileMap& terrain, const Player& player)
{
	TCODConsole::root->clear();

	terrain.Draw(tileSet);
	player.Draw();

	TCODConsole::root->print(67, 24, "Items");
	TCODConsole::root->print(62, 26, "A:");
	TCODConsole::root->print(62, 28, "B:");
	TCODConsole::root->print(62, 30, "C:");

	TCODConsole::root->print(66, 1, player.GetName().c_str());
	TCODConsole::root->print(2, 1, (std::string("FLOOR:") + std::to_string(1)).c_str());
	TCODConsole::root->print(12, 1, (std::string("LVL:") + std::to_string(player.GetLevel())).c_str());
	TCODConsole::root->print(20, 1, (std::string("EXP:") + std::to_string(player.GetLevel())).c_str());
	TCODConsole::root->print(32, 1, (std::string("HP:") + std::to_string(player.GetHealth()) + std::string("/") + std::to_string(player.GetMaxHealth())).c_str());

	TCODConsole::flush();
}